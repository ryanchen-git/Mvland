<h1>常見問題</h1>

<a name="1a"></a>
<div class="section">Mvland是什麼?</div>
Mvland是一個讓大家欣賞及分享音樂錄影帶(MV)的地方, MV就是Music Video的簡稱. Mvland所有的MV都是由網友提供, 這裡的MV都是藉由YouTube Embed的功能將YouTube裡的Music Vide上傳到Mvland與大家分享.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="1b"></a>
<div class="section">Mvland有哪些功能及特色?</div>
Mvland讓每個人以非常簡單的步驟提供自己喜歡的MV, 提供完後的MV馬上可以讓自己和所有人觀賞. 詳細的功能如下:
<ul>
<li>提供任何在YouTube裡找得到的MV</li>
<li>已提供的MV可以進行修改或刪除</li>
<li>加入任何MV到我的最愛以供方便日後觀賞</li>
<li>在任何MV頁面中發表意見/留言</li>
<li>提供MV着有權力刪除在自己提供的MV中的留言</li>
<li>透過Email轉寄MV給朋友/家人/同事</li>
<li>舉報有問題的MV</li>
</ul>
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2a"></a>
<div class="section">我要怎麼加入?</div>
加入Mvland非常簡單, 只要到<a href="http://www.mvland.com/signup">註冊首頁</a>輸入你要的帳號, 密碼和一些基本資料, 然後按'送出'就完成了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2b"></a>
<div class="section">我要怎麼知道我想用的帳號沒有和別人重複?</div>
在<a href="http://www.mvland.com/signup">註冊首頁</a>的帳號欄位先輸入你想要用的帳號, 然後在欄位旁邊點擊"帳號是否有人使用?"就可以查詢了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2c"></a>
<div class="section">註冊的程序是什麼?</div>
在<a href="http://www.mvland.com/signup">註冊首頁</a>完成註冊後, 你的Email將會收到一封Mvland寄給你的一封帳號啟動連結的信, 只要按照步驟完成帳號啟動就可以了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2d"></a>
<div class="section">我忘記我的帳號/密碼</div>
不用擔心, Mvland提供以下的解決辦法:
<ul>
	<li>忘記帳號: 請到<a href="http://www.mvland.com/forgot_username">忘記帳號</a>首頁填入你註冊的Email, 我們將會把你的帳號寄到你的Email信箱.</li>
	<li>忘記密碼: 請到<a href="http://www.mvland.com/forgot_password">忘記密碼</a>首頁填你註冊的帳號, 我們將會把重新設定密碼的步驟寄到你Email信箱.</li>
</ul>
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2e"></a>
<div class="section">我要怎麼修改我的帳號?</div>
請登入你的帳號並點選"修改我的個人資料"就可以進行帳號的修改.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2f"></a>
<div class="section">我要怎麼更改我的密碼?</div>
請登入你的帳號並點選"修改我的密碼"就可以進行密碼的更改.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="2g"></a>
<div class="section">我的最愛裡的MV為什麼不見了?</div>
如果你存到最愛裡的MV莫名其妙消失的話, 有以下兩種可能: 一是此MV已被原提供著移除掉, 二是此MV因違反Mvland規定而被我們移除掉了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3a"></a>
<div class="section">我要怎麼提供MV?</div>
為確保MV品質, Mvland目前只接受YouTube的Embed功能來讓網友提供MV. 詳細的MV提供方法如下:
<div style="padding-top: 10px;"></div>

<ul class="step_list">
	<li><span class="step">步驟1</span>:<br />
	在<a href="http://www.youtube.com/">YouTube</a>裡搜尋到你想要提供的MV, 並點選進入此MV的頁面.</li>
	
	<li><span class="step">步驟2</span>:<br />
	在頁面中錄影帶播放的下面按"崁入"會出現如下圖的視窗, 把崁入程式碼(紅色框內)用滑鼠複製起來(按滑鼠右鍵複製或鍵盤Ctrl+C)</li>
	<div style="padding-top: 10px;"></div>
	<img src="images/youtube_embed.jpg" style="border: 1px solid #999;"/>
		
	<li><span class="step">步驟3</span>:<br />
	回到Mvland的<a href="http://www.mvland.com/provide">提供MV</a>首頁把之前複製好的崁入程式碼貼到MV網址欄位就可以了.</li>
</ul>
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3b"></a>
<div class="section">提供MV有哪些限制?</div>
目前Mvland只接受網友利用YouTube Embed的功能將MV貼上來, 至於MV本身並沒有特別限制. 任何國家, 任何語言的MV都歡迎提供到Mvland來, 只要提供的MV和標題一致就可以了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3c"></a>
<div class="section">我找不到我想看的MV</div>
你可以利用Mvland的搜尋功能來找你想看的MV, 搜尋時請輸入歌手或歌曲名字. 如果找不到的話那表示可能這個MV還沒有任何人提供, 那何不當作第一個提供這個MV的人呢? 到<a href="http://www.youtube.com/">YouTube</a>搜尋這個MV並提供到Mvland來吧! 如果連YouTube都找不到的話那就有可能是歌曲太舊或是這首歌根本沒有拍MV.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3d"></a>
<div class="section">我要去哪裡找歌詞?</div>
歌詞一字一字打進去有點累人, 介紹大家一個不錯找到歌詞的地方, 那就是<a href="http://tw.knowledge.yahoo.com/" target="_blank">Yahoo奇摩知識</a>. 基本上在那裏沒有甚麼歌詞找不到的, 只要輸入歌名進去應該都找得到. 找到後把歌詞複製再貼到Mvland就可以了.
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3e"></a>
<div class="section">我要怎麼搜尋MV?</div>
你可以用下列的4種組合來搜尋Mvland的MV:
<ul>
	<li><strong>歌手名字</strong><br />如: <a href="http://www.mvland.com/search?mv_search=張惠妹">張惠妹</a></li>
	<li><strong>歌曲名稱</strong><br />如: <a href="http://www.mvland.com/search?mv_search=如果你也聽說">如果你也聽說</a></li>
	<li><strong>歌手名字+歌曲名稱</strong><br />如: <a href="http://www.mvland.com/search?mv_search=張惠妹如果你也聽說">張惠妹如果你也聽說</a></li>
	<li><strong>歌曲名稱+歌手名字</strong><br />如: <a href="http://www.mvland.com/search?mv_search=如果你也聽說張惠妹">如果你也聽說張惠妹</a></li>
</ul>
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3f"></a>
<div class="section">我發現有MV/歌詞的內容和標題不符</div>
如果你發現有網友提供的MV內容或歌詞和標題不符的話, 你可以進行檢舉. 方法是在每個MV下面都有一個'舉報問題'的選項, 你只要點選此連結並選擇舉報的原因就可以了. 一但我們收到舉報後會盡快做處理. 請注意'舉報問題'的選項僅限於會員才可以
執行. 
<div class="go_top"><a href="#top">回到最上面</a></div>

<a name="3g"></a>
<div class="section">我提供的MV為什麼不見了?</div>
如果你提供的MV忽然消失不見的話, 那麼表示你的MV有問題並已被Mvland移除. 一但有問題的MV被Mvland發現或是經由網友舉報後, 我們會立刻移除你的MV並且不會另行通知.
