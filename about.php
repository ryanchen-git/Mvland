<h1>關於Mvland</h1>

Mvland成立於2007年10月, 這是一個把音樂錄影帶(MV)集中在一起的網站, 其成立目的是希望讓大家有一個欣賞及分享MV與歌詞的地方. Mvland本身並不支援上傳MV的功能, 這裡所有的MV都是經由每個人利用YouTube Embed的功能將YouTube裡的Music Videos轉貼到Mvland上來與大家分享.<br /><br />

Mvland最大的特色是讓每個人以非常簡單的步驟提供自己喜歡的MV, 並在提供完後可以馬上觀賞. 我們希望每個到Mvland的人可以花最少的時間提供MV, 並且花大部份的時間欣賞MV.<br /><br />

我們將會繼續提供更多更方便的服務讓Mvland成為觀賞MV的最好地方, "簡單的分享, 好好的欣賞" 將會是我們不變的宗旨. 如果有任何意見請給予我們指教.

